/////////////////////////////////////////////////
// Functions to be called with onmouseover for //
// buttons, to change image on and off hover.  //
// The id of the button is passed.             //
/////////////////////////////////////////////////
function HoverOn(ID)
	{document.getElementById(ID).src= "path/to/image";}
function HoverOff(ID)
	{document.getElementById(ID).src="path/to/image";}
